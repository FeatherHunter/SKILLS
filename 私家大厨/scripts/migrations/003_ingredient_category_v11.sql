--
-- 改动前必答 3 问:
--   1. 影响哪些文件? 本文件(UPDATE 改 category 值) + ingredients 表
--   2. 有没有数据迁移? 是,UPDATE 现有 13 条调料记录
--   3. 回滚方案? 003_rollback.sql(恢复旧值)
--
-- 迁移规则(v5.2 边界):
--   原 "调料" 里的 中式基础主料 → "葱姜蒜":
--     生姜/姜/老姜 → 葱姜蒜
--     大蒜/蒜/蒜瓣 → 葱姜蒜
--     葱/小葱/香葱/大葱/葱花/蒜苗 → 葱姜蒜
--     姜末/蒜末/蒜蓉 → 葱姜蒜
--
-- 注:干粉末类(生姜粉/大蒜粉)不在 DB 当前数据里,如有应归"调料"(用户已确认)
-- 注:整株鲜香草(迷迭香/罗勒等)DB 当前数据里也没有,如有应归"香草"
-- 注:洋葱/西红柿已在 DB 中,category 都是"蔬菜",无需改

-- 葱姜蒜(中式基础主料整株/块/瓣 + 装饰末)
UPDATE ingredients SET category = '葱姜蒜'
WHERE name IN ('生姜', '姜', '老姜', '大蒜', '蒜', '蒜瓣', '蒜苗',
               '葱', '小葱', '香葱', '大葱', '葱花', '蒜蓉',
               '姜末', '蒜末')
  AND category = '调料';

